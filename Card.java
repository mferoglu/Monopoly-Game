public abstract class Card {
	
	private String nameOfTheCard;
	private String actionOfTheCard;
	private int cardID;
	
	
	public String getNameOfTheCard() {
		return nameOfTheCard;
	}

	public void setNameOfTheCard(String nameOfTheCard) {
		this.nameOfTheCard = nameOfTheCard;
	}

	public String getActionOfTheCard() {
		return actionOfTheCard;
	}

	public void setActionOfTheCard(String actionOfTheCard) {
		this.actionOfTheCard = actionOfTheCard;
	}

	public int getCardID() {
		return cardID;
	}

	public void setCardID(int cardID) {
		this.cardID = cardID;
	}
	
	
	

}
